clear all 
%==========================================================================
% inputting the variables
%==========================================================================

step = 0.1;
a = 20;
b = 0.2;
c= 2.*pi;

x1 = -15 : step : 30;
x2 = -15 : step : 30;

%==========================================================================
% generating the function
%==========================================================================

for i = 1:length(x1)
    for j = 1:length(x2)
        func(i,j) = -a.*exp(-b.*sqrt(0.5.*((x1(i).^2)+(x2(j).^2))))-exp(0.5.*(cos(x1(i).*c)+cos(x2(j).*c)))+a+exp(1);
    end
end

%==========================================================================
% plotting the mesh
%==========================================================================

mesh(x1,x2,func)
xlabel('x1')
ylabel('x2')
zlabel('Ackley Function') 
title('Ackley Mesh Plot')

%==========================================================================
% finding the minimum
%==========================================================================
% 
[Min,Index] = min(func(:));
[Index_row, Index_col] = ind2sub(size(func),Index);
x1(Index_row)
x2(Index_col)
